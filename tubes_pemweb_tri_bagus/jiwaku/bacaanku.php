                    terapi, termasuk terapi individual dan kelompok, serta pendidikan tentang bahaya alkohol dan cara 
                    menghindarinya.</p>
                
                <p>Selama terapi, Lisa menemukan bahwa peristiwa traumatis di masa lalunya adalah akar dari kecanduannya. 
