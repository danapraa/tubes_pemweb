                <h2>Pengertian Kesehatan Mental</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/jiwaku.jpg" alt="Diabetes Mellitus Image">
                <h3>Apa itu Gangguan Jiwa?</h3>
                <p>Gangguan jiwa adalah gangguan yang memengaruhi suasana hati, pola pikir, dan perilaku seseorang secara umum. Kondisi ini biasanya berkaitan dengan masalah dalam fungsi sosial, pekerjaan, atau masalah keluarga.</p>
                <p>Gangguan jiwa adalah kondisi yang perlu mendapatkan penanganan dengan segera. Sehingga, kesadaran masyarakat akan pentingnya kesehatan mental pada dirinya ataupun orang terdekatnya perlu ditingkatkan.</p>
                
                <h3>Perbedaan Gangguan Jiwa dan Gangguan Mental</h3>
                <p>Pada dasarnya, tidak ada perbedaan signifikan antara gangguan jiwa dan gangguan mental. Gangguan jiwa bisa terjadi apabila gangguan mental yang dialami seseorang tidak ditangani dan bertambah buruk. Baik gangguan mental atau gangguan jiwa adalah kondisi yang membutuhkan konsultasi dan penanganan segera dengan seorang psikolog atau psikiater (dokter spesialis kejiwaan) agar bisa membaik.</p>
                
                <h3>Penyebab Gangguan Jiwa</h3>
                <p>Pada beberapa kasus, tidak diketahui secara pasti apa penyebab gangguan jiwa. Akan tetapi, kondisi ini dapat dipengaruhi oleh sejumlah faktor, yakni faktor genetik, lingkungan sekitar, maupun gabungan dari kedua faktor tersebut, seperti:</p>
                <ul>
                    <li>Memiliki keluarga dengan riwayat gangguan jiwa.</li>
                    <li>Persoalan hidup pribadi.</li>
                    <li>Konsumsi alkohol secara berlebihan dan menggunakan obat-obatan terlarang.</li>
                    <li>Terkena paparan virus, minuman keras, racun, atau obat-obatan saat berada dalam kandungan.</li>
                    <li>Menderita penyakit kronis.</li>
                    <li>Memiliki pengalaman traumatis dalam hidup.</li>
                    <li>Mengalami kerusakan otak.</li>
                    <li>Merasa kesepian dan mempunyai hidup yang penuh dengan tekanan.</li>
                    <li>Pernah mengalami gangguan jiwa sebelumnya.</li>
                    <li>Perubahan reaksi senyawa kimia dalam otak yang berpengaruh pada suasana hati serta berbagai aspek kesehatan mental.</li>
                </ul>

                <h3>Gejala Gangguan Jiwa</h3>
                <p>Gejala gangguan jiwa berbeda-beda, tergantung dari jenisnya. Namun, sejumlah gejala umum yang biasanya dialami oleh pengidap gangguan jiwa adalah:</p>
                <ul>
                    <li>Mengalami delusi.</li>
                    <li>Mengalami perubahan suasana hati secara drastis, misalnya dari yang gembira menjadi sangat sedih.</li>
                    <li>Sering merasa takut secara berlebihan.</li>
                    <li>Cenderung menyendiri dan menarik diri dari lingkungan sosial.</li>
                    <li>Sering merasa emosional dan amarah yang tidak terkendali bahkan sampai melakukan tindak kekerasan.</li>
                </ul>
                <p>Selain gejala psikologis, pengidap gangguan jiwa terkadang juga mengalami gejala secara fisik, seperti nyeri kepala, nyeri punggung, nyeri perut, atau nyeri pada area tubuh lain tanpa penyebab yang jelas.</p>
                
                <h3>Jenis-Jenis Gangguan Jiwa</h3>
                <p>Gangguan jiwa terbagi menjadi beberapa jenis, di mana setiap kelompok tersebut terbagi lagi menjadi beberapa jenis yang lebih spesifik. Berikut adalah jenis-jenis gangguan jiwa:</p>
                
                <h4>1. Gangguan Cemas</h4>
                <p>Gangguan cemas ditandai dengan kecemasan saat merespon situasi atau objek tertentu yang sulit untuk dikendalikan, sehingga menyebabkan pengidapnya merasa takut dan panik hingga jantungnya berdetak lebih cepat. Kecemasan bisa berupa gangguan panik (panic attack), fobia, dan gangguan cemas sosial.</p>
                
                <h4>2. Ketidakmampuan Mengontrol Keinginan</h4>
                <p>Jenis gangguan jiwa ini membuat pengidapnya tidak dapat mengontrol dorongan untuk melakukan sesuatu yang negatif dan berbahaya untuk dirinya sendiri maupun orang lain. Kondisi yang masuk dalam jenis gangguan jiwa ini contohnya adalah kleptomania, yaitu keinginan untuk mencuri atau mengambil barang-barang tanpa izin, adiksi atau kecanduan alkohol dan obat-obatan terlarang, belanja berlebihan, atau suka menyulut api (piromania).</p>
                
                <h4>3. Gangguan Makan</h4>
                <p>Gangguan makan menyebabkan penderitanya mengalami perubahan perilaku, kebiasaan, dan emosi yang berhubungan dengan berat badan serta makanan. Adapun beberapa bentuk gangguan makan di antaranya anoreksia, bulimia, dan binge eating disorder.</p>
                
                <h4>4. Gangguan Psikotik</h4>
                <p>Gangguan psikotik dapat mengarahkan kesadaran dan pikiran manusia secara abnormal. Akibatnya, timbul halusinasi dan delusi yang sebenarnya tidak ada atau tidak terjadi sehingga akhirnya mengganggu aktivitas sehari-hari. Gangguan psikotik contohnya dapat berupa erotomania, yaitu kondisi di mana seseorang mengalami delusi bahwa ada orang yang mencintainya, namun pada kenyataannya tidak.</p>
                
                <h4>5. Gangguan Mood atau Afektif</h4>
                <p>Gangguan ini ditandai dengan perubahan suasana hati dalam waktu yang sangat singkat, contohnya gangguan bipolar dan depresi.</p>
                
                <h4>6. Gangguan Disosiatif</h4>
                <p>Gangguan disosiatif atau gangguan kepribadian ganda atau lebih adalah kondisi ketika seseorang mengalami gangguan pada ingatan, identitias, dan kesadaran terhadap diri sendiri serta lingkungan yang ditempatinya. Masing-masing kepribadian tersebut dapat mengambil alih kesadaran pengidapnya. Gangguan ini biasanya dipicu oleh peristiwa traumatis.</p>
                
                <h4>7. PTSD (Gangguan Stres Pasca Trauma)</h4>
                <p>PTSD adalah gangguan jiwa yang bisa terjadi akibat adanya pengalaman traumatis, misalnya pelecehan seksual, bencana alam, kematian orang yang dicintai, atau kecelakaan.</p>
                
                <h3>Cara Mengatasi Gangguan Jiwa</h3>
                <p>Secara umum, gangguan jiwa adalah kondisi yang tidak bisa sembuh atau hilang dengan sendirinya, sehingga perlu mendapatkan bantuan dan penanganan langsung dari dokter. Bahkan, kondisi ini bisa memburuk apabila tidak segera ditangani ataupun diobati.</p>
                <p>Dokter spesialis kejiwaan atau psikiater dapat memberikan psikoterapi atau konseling serta obat-obatan antidepresan, antipsikotik, dan anticemas yang disesuaikan dengan jenis gangguan jiwa pengidapnya. Pengidap biasanya juga disarankan untuk menjalani terapi, seperti psikoterapi, simulasi otak, atau perawatan di rumah sakit jiwa apabila dibutuhkan.</p>
                <p>Selain itu, dukungan dari keluarga dan rekan terdekat juga sangat dibutuhkan oleh pengidap gangguan jiwa agar bisa kembali menjalankan aktivitasnya dengan normal.</p>
                <p>Itulah penjelasan seputar kondisi gangguan jiwa dan jenis-jenisnya. Penting untuk diingat bahwa gangguan jiwa bukanlah tanda kelemahan atau kegagalan pribadi. Banyak gangguan jiwa dapat diobati melalui terapi psikologis, dukungan sosial, atau pemberian obat-obatan.</p>
