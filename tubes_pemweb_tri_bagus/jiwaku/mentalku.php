                <h2>Pengertian Kesehatan Mental</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/mental.jpg" alt="Diabetes Mellitus Image">
                <h3>Penjelasan</h3>
                <p>Kesehatan jiwa atau sebutan lainnya kesehatan mental adalah kesehatan yang berkaitan dengan kondisi emosi, kejiwaan, dan psikis seseorang.</p>
                
                <p>Perlu kamu ketahui bahwa peristiwa dalam hidup yang berdampak besar pada kepribadian dan perilaku seseorang bisa berpengaruh pada kesehatan mentalnya.</p>

                <p>Misalnya, pelecehan saat usia dini, stres berat dalam jangka waktu lama tanpa adanya penanganan, dan mengalami kekerasan dalam rumah tangga.</p>

                <p>Berbagai kondisi tersebut bisa membuat kondisi kejiwaan seseorang terganggu, sehingga muncul gejala gangguan kesehatan jiwa.</p>

                <p>Akan tetapi, masalah kesehatan mental bisa mengubah cara seseorang dalam mengatasi stres, berhubungan dengan orang lain, membuat pilihan, dan memicu hasrat untuk menyakiti diri sendiri.</p>

                <p>Beberapa jenis gangguan mental yang umum terjadi antara lain depresi, gangguan bipolar, kecemasan, gangguan stres pasca trauma (PTSD), gangguan obsesif kompulsif (OCD), dan psikosis.</p>

                <p>Selain itu, ada beberapa penyakit mental hanya terjadi pada jenis pengidap tertentu, seperti postpartum depression hanya menyerang ibu setelah melahirkan.</p>

                <h3>Apa Saja Penyebab Gangguan Kesehatan Mental?</h3>

                <p>Ada beberapa kondisi yang bisa menjadi penyebab seseorang mengalami gangguan kesehatan jiwa, antara lain:</p>
                <ul>
                    <li>Cedera pada kepala.</li>
                    <li>Faktor genetik atau terdapat riwayat pengidap gangguan kesehatan jiwa dalam keluarga.</li>
                    <li>Kekerasan dalam rumah tangga atau bentuk pelecehan lainnya.</li>
                    <li>Adanya riwayat kekerasan saat kanak-kanak.</li>
                    <li>Memiliki kelainan senyawa kimia otak atau gangguan pada otak.</li>
                    <li>Mengalami diskriminasi dan stigma.</li>
                    <li>Kehilangan atau kematian seseorang yang sangat dekat.</li>
                    <li>Mengalami kerugian sosial, seperti masalah kemiskinan atau utang.</li>
                    <li>Merawat anggota keluarga atau teman yang sakit kronis.</li>
                    <li>Pengangguran, kehilangan pekerjaan, atau tunawisma.</li>
                    <li>Pengaruh zat racun, alkohol, atau obat-obatan yang dapat merusak otak.</li>
                    <li>Stres berat yang terjadi dalam waktu yang lama.</li>
                    <li>Terisolasi secara sosial atau merasa kesepian.</li>
                    <li>Tinggal pada lingkungan perumahan yang buruk.</li>
                    <li>Mengalami trauma yang signifikan, seperti pertempuran militer, kecelakaan serius, atau tindakan kriminal lainnya.</li>
                </ul>

                <h3>Faktor Risiko Gangguan Kesehatan Mental</h3>

                <p>Selain itu, ada beberapa faktor yang bisa meningkatkan risiko seseorang mengalami gangguan kesehatan jiwa.</p>
                <ul>
                    <li>Wanita berisiko tinggi mengidap depresi dan kecemasan, sedangkan laki-laki memiliki risiko mengidap ketergantungan zat dan antisosial.</li>
                    <li>Wanita setelah melahirkan. Baca lebih lanjut artikel Mengenal 3 Jenis Depresi Pasca-Melahirkan untuk mengetahui apa saja jenis gangguan mental yang kerap terjadi pada ibu setelah melahirkan.</li>
                    <li>Adanya masalah pada masa kanak-kanak atau masalah gaya hidup.</li>
                    <li>Menjalani profesi yang memicu stres, seperti dokter dan pengusaha.</li>
                    <li>Memiliki riwayat anggota keluarga atau keluarga dengan penyakit mental.</li>
                    <li>Mempunyai riwayat kelahiran dengan kelainan pada otak.</li>
                    <li>Adanya riwayat penyakit mental sebelumnya.</li>
                    <li>Mengalami kegagalan dalam hidup, seperti sekolah atau kehidupan kerja.</li>
                    <li>Menyalahgunakan alkohol atau obat-obatan terlarang.</li>
                </ul>

                <h3>Gejala Gangguan Kesehatan Mental</h3>

                <p>Gejala gangguan kesehatan jiwa bisa berbeda bergantung pada jenisnya.</p>

                <p>Kendati demikian, gejala umum dari kelainan kesehatan ini yang bisa kamu kenali antara lain:</p>
                <ul>
                    <li>Berteriak atau berkelahi dengan keluarga dan teman-teman.</li>
                    <li>Delusi, paranoia, atau halusinasi.</li>
                    <li>Kehilangan kemampuan untuk berkonsentrasi.</li>
                    <li>Ketakutan, kekhawatiran, atau perasaan bersalah yang selalu menghantui.</li>
                    <li>Ketidakmampuan untuk mengatasi stres atau masalah sehari-hari.</li>
                    <li>Marah berlebihan dan rentan melakukan kekerasan.</li>
                    <li>Memiliki pengalaman dan kenangan buruk yang tidak dapat dilupakan.</li>
                    <li>Adanya pikiran untuk menyakiti diri sendiri atau orang lain.</li>
                    <li>Menarik diri dari orang-orang dan kegiatan sehari-hari.</li>
                    <li>Mendengar suara atau mempercayai sesuatu yang tidak benar.</li>
                    <li>Mengalami nyeri yang tidak dapat dijelaskan.</li>
                    <li>Menunjukkan perubahan suasana hati secara mendadak yang menyebabkan masalah dalam hubungan dengan orang lain.</li>
                    <li>Merasa bingung, pelupa, marah, tersinggung, cemas, kesal, khawatir, dan takut yang tidak biasa.</li>
                    <li>Perasaan sedih, tidak berarti, tidak berdaya, putus asa, atau tanpa harapan.</li>
                    <li>Merokok, minum alkohol lebih banyak dari biasanya, atau bahkan menggunakan narkoba.</li>
                    <li>Perubahan drastis dalam kebiasaan makan, seperti makan terlalu banyak atau terlalu sedikit.</li>
                    <li>Perubahan gairah seks.</li>
                    <li>Rasa lelah yang signifikan, energi menurun, atau mengalami masalah tidur.</li>
                    <li>Tidak dapat melakukan aktivitas sehari-hari seperti merawat anak atau pergi ke sekolah atau tempat kerja.</li>
                    <li>Tidak mampu memahami situasi dan orang-orang.</li>
                </ul>
