    Apa itu Gangguan Jiwa?

            Gangguan jiwa adalah kondisi yang memengaruhi pola pikir, perasaan, perilaku, 
            atau aktivitas sehari-hari pengidapnya. Gangguan jiwa dapat bervariasi dari kondisi 
            yang ringan hingga parah, dan dapat memengaruhi orang dari segala usia, latar belakang, 
            atau kelompok sosial. Gangguan jiwa terbagi menjadi beberapa jenis dengan gejala
            yang berbeda-beda. 

            Perbedaan Gangguan Jiwa dan Gangguan Mental
 

Pada dasarnya, tidak ada perbedaan signifikan antara gangguan jiwa dan gangguan mental. Gangguan jiwa bisa terjadi apabila gangguan mental yang dialami seseorang tidak ditangani dan bertambah buruk. Baik gangguan mental atau gangguan jiwa adalah kondisi yang membutuhkan konsultasi dan penanganan segera dengan seorang psikolog atau psikiater (dokter spesialis kejiwaan) agar bisa membaik. 

 

Penyebab Gangguan Jiwa
 

Pada beberapa kasus, tidak diketahui secara pasti apa penyebab gangguan jiwa. Akan tetapi, kondisi ini dapat dipengaruhi oleh sejumlah faktor, yakni faktor genetik, lingkungan sekitar, maupun gabungan dari kedua faktor tersebut, seperti:

 

Memiliki keluarga dengan riwayat gangguan jiwa.
Persoalan hidup pribadi.
Konsumsi alkohol secara berlebihan dan menggunakan obat-obatan terlarang.
Terkena paparan virus, minuman keras, racun, atau obat-obatan saat berada dalam kandungan.
Menderita penyakit kronis.
Memiliki pengalaman traumatis dalam hidup.
Mengalami kerusakan otak.
Merasa kesepian dan mempunyai hidup yang penuh dengan tekanan.
Pernah mengalami gangguan jiwa sebelumnya.
Perubahan reaksi senyawa kimia dalam otak yang berpengaruh pada suasana hati serta berbagai aspek kesehatan mental.

Gejala Gangguan Jiwa
Gejala gangguan jiwa berbeda-beda, tergantung dari jenisnya. Namun, sejumlah gejala umum yang biasanya dialami oleh pengidap gangguan jiwa adalah:
Mengalami delusi.
Mengalami perubahan suasana hati secara drastis, misalnya dari yang gembira menjadi sangat sedih.
Sering merasa takut secara berlebihan.
Cenderung menyendiri dan menarik diri dari lingkungan sosial.
Sering merasa emosional dan amarah yang tidak terkendali bahkan sampai melakukan tindak kekerasan.

Selain gejala psikologis, pengidap gangguan jiwa terkadang juga mengalami gejala secara fisik, seperti nyeri kepala, nyeri punggung, nyeri perut, atau nyeri pada area tubuh lain tanpa penyebab yang jelas.

Jenis-Jenis Gangguan Jiwa
Gangguan jiwa terbagi menjadi beberapa jenis, di mana setiap kelompok tersebut terbagi lagi menjadi beberapa jenis yang lebih spesifik. Berikut adalah jenis-jenis gangguan jiwa:

1. Gangguan Cemas

Gangguan cemas ditandai dengan kecemasan saat merespon situasi atau objek tertentu yang sulit untuk dikendalikan, sehingga menyebabkan pengidapnya merasa takut dan panik hingga jantungnya berdetak lebih cepat. Kecemasan bisa berupa gangguan panik (panic attack), fobia,  dan gangguan cemas sosial.

2. Ketidakmampuan Mengontrol Keinginan

Jenis gangguan jiwa ini membuat pengidapnya tidak dapat mengontrol dorongan untuk melakukan sesuatu yang negatif dan berbahaya untuk dirinya sendiri maupun orang lain. Kondisi yang masuk dalam jenis gangguan jiwa ini contohnya adalah kleptomania, yaitu keinginan untuk mencuri atau mengambil barang-barang tanpa izin, adiksi atau kecanduan alkohol dan obat-obatan terlarang, belanja berlebihan, atau suka menyulut api (piromania). 

3. Gangguan Makan

Gangguan makan menyebabkan penderitanya mengalami perubahan perilaku, kebiasaan, dan emosi yang berhubungan dengan berat badan serta makanan. Adapun beberapa bentuk gangguan makan di antaranya anoreksia, bulimia, dan binge eating disorder.

4. Gangguan Psikotik

Gangguan psikotik dapat mengarahkan kesadaran dan pikiran manusia secara abnormal. Akibatnya, timbul halusinasi dan delusi yang sebenarnya tidak ada atau tidak terjadi sehingga akhirnya mengganggu aktivitas sehari-hari. Gangguan psikotik contohnya dapat berupa erotomania, yaitu kondisi di mana seseorang mengalami delusi bahwa ada orang yang mencintainya, namun pada kenyataannya tidak. 

5. Gangguan Mood atau Afektif

Gangguan ini ditandai dengan perubahan suasana hati dalam waktu yang sangat singkat, contohnya gangguan bipolar dan depresi.

6. Gangguan Disosiatif

Gangguan disosiatif atau gangguan kepribadian ganda atau lebih adalah kondisi ketika seseorang mengalami gangguan pada ingatan, identitias, dan kesadaran terhadap diri sendiri serta lingkungan yang ditempatinya. Masing-masing kepribadian tersebut dapat mengambil alih  kesadaran pengidapnya. Gangguan ini biasanya dipicu oleh peristiwa traumatis.

7. PTSD (Gangguan Stres Pasca Trauma)

PTSD adalah gangguan jiwa yang bisa terjadi akibat adanya pengalaman traumatis, misalnya pelecehan seksual, bencana alam, kematian orang yang dicintai, atau kecelakaan.
Cara Mengatasi Gangguan Jiwa
Secara umum, gangguan jiwa adalah kondisi yang tidak bisa sembuh atau hilang dengan sendirinya, sehingga perlu mendapatkan bantuan dan penanganan langsung dari dokter. Bahkan, kondisi ini bisa memburuk apabila tidak segera ditangani ataupun diobati.
Dokter spesialis kejiwaan atau psikiater dapat memberikan psikoterapi atau konseling serta obat-obatan antidepresan, antipsikotik, dan anticemas yang disesuaikan dengan jenis gangguan jiwa pengidapnya. Pengidap biasanya juga disarankan untuk menjalani terapi, seperti psikoterapi, simulasi otak, atau perawatan di rumah sakit jiwa apabila dibutuhkan.
Selain itu, dukungan dari keluarga dan rekan terdekat juga sangat dibutuhkan oleh pengidap gangguan jiwa agar bisa kembali menjalankan aktivitasnya dengan normal.
Itulah penjelasan seputar kondisi gangguan jiwa dan jenis-jenisnya. Penting untuk diingat bahwa gangguan jiwa bukanlah tanda kelemahan atau kegagalan pribadi. Banyak gangguan jiwa dapat diobati melalui terapi psikologis, dukungan sosial, atau pemberian obat-obatan.



