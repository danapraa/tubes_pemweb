                <h2>Asma</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/asma.jpg" alt="Asma Image">
                <h3>Penjelasan</h3>
                <p>Asma adalah penyakit kronis yang mempengaruhi saluran udara di paru-paru, 
                    menyebabkan kesulitan bernapas, batuk, dan mengi.</p>
            <h3>Penyebab</h3>
            <p>Faktor genetik: Riwayat keluarga dengan asma.</p>
            <p>Alergen: Seperti debu, serbuk sari, dan bulu hewan.</p>
            <p>Infeksi pernapasan: Dapat memicu serangan asma.</p>
            <p>Polusi udara: Iritan yang dapat memperburuk asma.</p>
            <p>Aktivitas fisik berlebihan: Bisa memicu serangan asma.</p>

            <h3>Penyembuhan</h3>
            <p>Inhaler bronkodilator: Untuk meredakan gejala akut.</p>
            <p>Inhaler kortikosteroid: Untuk mengontrol peradangan.</p>
            <p>Menghindari pemicu: Seperti alergen dan iritan.</p>

            <h3>Pencegahan</h3>
            <p>Menghindari alergen dan iritan: Mengurangi risiko serangan asma.</p>
            <p>Menggunakan masker: Di lingkungan berdebu atau berpolusi.</p>
            <p>Menjaga kebersihan rumah: Mengurangi paparan alergen.</p>
            <p>Vaksinasi: Flu dan pneumonia untuk mencegah infeksi yang memicu asma.</p>
