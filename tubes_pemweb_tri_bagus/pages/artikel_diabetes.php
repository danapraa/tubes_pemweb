                <h2>Diabetes Mellitus</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/diabetes.jpg" alt="Diabetes Mellitus Image">
                <h3>Penjelasan</h3>
                <p>Diabetes mellitus adalah kondisi kronis di mana kadar gula darah (glukosa) terlalu tinggi. 
                    Ada dua tipe utama: Tipe 1 (ketika tubuh tidak memproduksi insulin) dan 
                    Tipe 2 (ketika tubuh tidak menggunakan insulin dengan baik atau tidak cukup memproduksinya).</p>
                
                <h3>Penyebab</h3>
                <p>Gaya hidup tidak sehat: Pola makan tinggi gula dan lemak, kurang olahraga.</p>
                <p>Faktor genetik: Riwayat keluarga dengan diabetes.</p>
                <p>Obesitas: Berat badan berlebih meningkatkan resistensi insulin.</p>
                <p>Gangguan insulin: Tubuh tidak memproduksi atau menggunakan insulin dengan baik.</p>

                <h3>Penyembuhan</h3>
                <p>Diet dan Olahraga: Mengontrol asupan makanan dan melakukan aktivitas fisik secara rutin.</p>
                <p>Obat-obatan: Metformin dan obat penurun gula darah lainnya.</p>
                <p>Terapi insulin: Untuk penderita diabetes tipe 1 dan beberapa kasus tipe 2.</p>
                <p>Pemantauan rutin: Mengecek kadar gula darah secara berkala.</p>

                <h3>Pencegahan</h3>
                <p>Menjaga berat badan sehat: Mengurangi risiko obesitas.</p>
                <p>Pola makan seimbang: Mengurangi konsumsi gula dan lemak.</p>
                <p>Olahraga teratur: Membantu mengontrol berat badan dan meningkatkan sensitivitas insulin.</p>
                <p>Pemantauan kadar gula: Terutama bagi mereka dengan riwayat keluarga diabetes.</p>
