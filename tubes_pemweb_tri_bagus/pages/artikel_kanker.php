                <h2>Hipertensi</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/hipertensi.jpg" alt="Hipertensi Image">
                <h3>Penjelasan</h3>
                <p>Hipertensi adalah kondisi di mana tekanan darah dalam arteri meningkat secara kronis, 
                    yang dapat menyebabkan masalah serius seperti penyakit jantung dan stroke.</p>

            <h3>Penyebab</h3>
            <p>Pola makan tinggi garam: Garam dapat meningkatkan tekanan darah.</p>
            <p>Kurangnya aktivitas fisik: Dapat menyebabkan obesitas dan meningkatkan risiko hipertensi.</p>
            <p>Kegemukan: Berat badan berlebih meningkatkan beban pada arteri.</p>
            <p>Stres: Dapat meningkatkan tekanan darah sementara.</p>
            <p>Faktor genetik: Riwayat keluarga dengan hipertensi.</p>

            <h3>Penyembuhan</h3>
            <p>Obat antihipertensi: Seperti ACE inhibitors, beta-blockers.</p>
            <p>Perubahan gaya hidup: Diet rendah garam, olahraga, pengelolaan stres.</p>
            <p>Pemantauan rutin: Mengecek tekanan darah secara berkala.</p>

            <h3>Pencegahan</h3>
            <p>Mengurangi asupan garam: Membantu menurunkan tekanan darah.</p>
            <p>Olahraga teratur: Meningkatkan kesehatan jantung.</p>
            <p>Menjaga berat badan: Mencegah obesitas.</p>
            <p>Menghindari alkohol dan rokok: Faktor risiko utama hipertensi.</p>
            <p>Mengelola stres: Teknik relaksasi seperti meditasi dan yoga.</p>
